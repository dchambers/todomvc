/*global React, Router*/
var app = app || {};

(function () {
	'use strict';

	var TodoApp = app.TodoApp;
	var model = new app.TodoModel('react-todos');

	function render() {
		React.render(
			<TodoApp model={model}/>,
			document.getElementsByClassName('todoapp')[0]
		);
	}

	model.subscribe(render);
	render();
})();
